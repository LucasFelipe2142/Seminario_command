package core;

/**
 * Classe que representa uma TV.
 */
public class TV {
    /**
     * Liga a TV.
     */
    public void turnOn() {
        System.out.println("TV ligada");
    }

    /**
     * Desliga a TV.
     */
    public void turnOff() {
        System.out.println("TV desligada");
    }
}
